create or replace function public.is_factory_admin()
returns boolean
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  current_uid uuid;
begin
  current_uid := auth.uid();

  if current_uid is not null and exists (
    select 1
    from public.user_access_levels membership
    join public.access_levels level on level.id = membership.access_level_id
    where membership.user_id = current_uid
      and membership.status = 'active'
      and level.can_access_admin = true
  ) then
    return true;
  end if;

  return
    coalesce((auth.jwt() -> 'app_metadata' ->> 'role') in ('admin', 'superadmin', 'owner'), false)
    or coalesce((auth.jwt() -> 'user_metadata' ->> 'role') in ('admin', 'superadmin', 'owner'), false)
    or coalesce((auth.jwt() -> 'app_metadata' ->> 'is_admin') in ('true', '1'), false)
    or coalesce((auth.jwt() -> 'user_metadata' ->> 'is_admin') in ('true', '1'), false);
end;
$$;

create or replace function public.assign_default_access_level()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  default_access_level_id bigint;
begin
  select id
  into default_access_level_id
  from public.access_levels
  where is_default = true
  order by priority asc
  limit 1;

  if default_access_level_id is not null then
    insert into public.user_access_levels (
      user_id,
      access_level_id,
      status,
      assigned_at,
      notes
    )
    values (
      new.id,
      default_access_level_id,
      'active',
      now(),
      'Auto assigned default access level'
    )
    on conflict (user_id) do nothing;
  end if;

  return new;
end;
$$;

