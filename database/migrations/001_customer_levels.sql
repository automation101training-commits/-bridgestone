create or replace function public.set_current_timestamp_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.access_levels (
  id bigint generated by default as identity primary key,
  code text not null unique,
  name text not null,
  description text,
  priority integer not null default 100,
  is_default boolean not null default false,
  can_view_dashboard boolean not null default true,
  can_manage_operations boolean not null default false,
  can_access_admin boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint access_levels_code_format check (code ~ '^[a-z0-9_\-]+$')
);

create unique index if not exists access_levels_one_default_idx
on public.access_levels (is_default)
where is_default = true;

create table if not exists public.user_access_levels (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  access_level_id bigint not null references public.access_levels(id) on delete restrict,
  status text not null default 'active',
  assigned_by uuid references auth.users(id) on delete set null,
  assigned_at timestamptz not null default now(),
  expires_at timestamptz,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint user_access_levels_status_check check (status in ('active', 'inactive', 'expired', 'suspended')),
  constraint user_access_levels_one_level_per_user unique (user_id)
);

create index if not exists user_access_levels_access_level_id_idx
on public.user_access_levels (access_level_id);

create index if not exists user_access_levels_status_idx
on public.user_access_levels (status);

